package ke.co.milleradulu.milleradulu.fadhili.apihandler.models;

public class CollectionCenter {
  private String collectionCenterId, collectionCenterName, collectionCenterLongitude, collectionCenterLatitude,
    createdAt, updatedAt;

  public String getCollectionCenterId() {
    return collectionCenterId;
  }

  public void setCollectionCenterId(String collectionCenterId) {
    this.collectionCenterId = collectionCenterId;
  }

  public String getCollectionCenterName() {
    return collectionCenterName;
  }

  public void setCollectionCenterName(String collectionCenterName) {
    this.collectionCenterName = collectionCenterName;
  }

  public String getCollectionCenterLongitude() {
    return collectionCenterLongitude;
  }

  public void setCollectionCenterLongitude(String collectionCenterLongitude) {
    this.collectionCenterLongitude = collectionCenterLongitude;
  }

  public String getCollectionCenterLatitude() {
    return collectionCenterLatitude;
  }

  public void setCollectionCenterLatitude(String collectionCenterLatitude) {
    this.collectionCenterLatitude = collectionCenterLatitude;
  }

  public String getCreatedAt() {
    return createdAt;
  }

  public void setCreatedAt(String createdAt) {
    this.createdAt = createdAt;
  }

  public String getUpdatedAt() {
    return updatedAt;
  }

  public void setUpdatedAt(String updatedAt) {
    this.updatedAt = updatedAt;
  }
}
